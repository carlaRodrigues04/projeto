<h1>Sobre a loja</h1>
<p>Criada por Carla Rodrigues e Giovana Xisto alunas do Instituto federal - Câmpus Itapetininga, cursantes no segundo ano do ensino médio<br>
     integrado ao técnico em informática, a loja com o nome de Netshirts se tratará de uma página para vendas de camisetas personalizadas<br>
      de filmes e séries.</p>

<h2>Missão</h2>
<p>Nossa missão é oferecer opções de vestuários personalizados
para os fanáticos por filmes e séries.</p>

<h2>Valores</h2>
<p>Temos como valores procurar a inovação do mercado, qualidade do produto,
preços justos e diversidade de opções.</p>

<h2>Visão</h2>
<p>Esperamos ter feito mais inovações no mercado e obter o reconhecimento como uma empresa de qualidade,
com bom atendimento aos clientes.</p>

